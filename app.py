@st.cache_resource
def init_model():
    return load_model()

tokenizer, model = init_model()

# UI
st.title(T["title"])
st.write(T["description"])

with st.form("farmer_form"):
    col1, col2 = st.columns(2)
    with col1:
        age = st.number_input(T["age"], min_value=18, max_value=100, step=1)
        land_type = st.selectbox(
            T["land_type"],
            options=L["land_types"],
            index=None,
            placeholder="Select Land Type"
        )
        land_size = st.text_input(T["land_size"], placeholder="Enter land size")
    with col2:
        state = st.selectbox(
            T["state"],
            options=L["states"],
            index=None,
            placeholder="Select State"
        )
        crops = st.multiselect(T["crops"], L["crops"])

    submitted = st.form_submit_button(T["submit"])

if submitted:
    if not state or not state.strip() or not land_size or not land_size.strip() or not crops:
        st.warning(T["warning_empty"])
    else:
        try:
            float(land_size)
        except ValueError:
            st.warning(T["warning_invalid_size"])
            st.stop()

        user_prompt = L["prompt"](age, state, land_size, land_type, crops)

        with st.spinner(T["loading"]):
            result = get_response(user_prompt, tokenizer, model)

        st.markdown(T["result"])
        st.success(result)
